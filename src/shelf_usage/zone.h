inline void *Zone::from_Offset(Offset p) {
    if (p == 0)
        return NULL;
    else
        return (char *)shelf_location_ptr + p;
}

inline Offset Zone::to_Offset(void *p) {
    if (p == NULL)
        return 0;
    else
        return (char *)p - (char *)shelf_location_ptr;
}

